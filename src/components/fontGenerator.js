function getWordPlayback(word, middle) {
    const startY = middle[1] - 2;
    const letters = word.toLowerCase().split("");
    const playback = [];
    const totalWidth = letters.map((value) => pixelFont[value].width).reduce((previousValue, currentValue) => previousValue + currentValue, 0) + letters.length - 1;
    const startX = middle[0] - Math.floor(totalWidth / 2);
    let width = startX;
    if (width < 0) {
        width = 0;
    }
    letters.forEach((letter) => {
        const letterSymbol = pixelFont[letter];
        const translatedPlayback = letterSymbol.playback.map((currentVertex) => [currentVertex[0] + width, currentVertex[1] + startY]);
        width += letterSymbol.width + 1;
        playback.push(...translatedPlayback);
    });
    const topLeftCorner = [startX - 2, startY - 2];
    const bottomRightCorner = [width, startY + 6];
    const lineCreator = (startVertex, length, direction) => {
        for (let i = 0; i < length; i++) {
            playback.push([startVertex[0] + direction[0] * i, startVertex[1] + direction[1] * i]);
        }
    };
    lineCreator(topLeftCorner, bottomRightCorner[0] - topLeftCorner[0], [1, 0]);
    lineCreator(bottomRightCorner, bottomRightCorner[0] - topLeftCorner[0], [-1, 0]);
    lineCreator(topLeftCorner, bottomRightCorner[1] - topLeftCorner[1] + 1, [0, 1]);
    lineCreator(bottomRightCorner, bottomRightCorner[1] - topLeftCorner[1] + 1, [0, -1]);
    return ({ playback: playback, width: width - middle[0], startPoint: topLeftCorner });
}
export { getWordPlayback };
